// ─── Message handler ──────────────────────────────────────
bot.on("message", async (msg) => {
  const chatId = msg.chat.id;
  const text = msg.text?.trim();

  if (!text) return;

  // Commands
  if (text === "/start") {
    return bot.sendMessage(chatId,
      "🏢 *ברוש AI — סוכן פנימי*\n\nשאל אותי על:\n• דירות ושטחים\n• יועצים ואנשי קשר\n• סטטוס פרויקטים\n\nפקודות:\n/reset — אפס שיחה\n/help — עזרה",
      { parse_mode: "Markdown" }
    );
  }

  if (text === "/reset") {
    sessions.delete(chatId);
    return bot.sendMessage(chatId, "✅ השיחה אופסה. מוכן לשאלות חדשות!");
  }

  if (text === "/help") {
    return bot.sendMessage(chatId,
      "💡 *דוגמאות לשאלות:*\n\n• מה שטח דירה 15 ביהודה המכבי?\n• מי האדריכל של פישמן מימון 16?\n• כמה יחידות יזם יש בביאליק 36?\n• מה הטלפון של קרן ברק?\n• כמה דירות יש בפרויקט רמב\"ם?",
      { parse_mode: "Markdown" }
    );
  }

  // Show typing indicator
  bot.sendChatAction(chatId, "typing");

  // Add to history and call Claude
  addToHistory(chatId, "user", text);

  try {
    const response = await anthropic.messages.create({
      model: "claude-sonnet-5",
      max_tokens: 600,
      system: SYSTEM_PROMPT,
      messages: getHistory(chatId),
    });

    const reply = response.content[0]?.text || "מצטער, לא הצלחתי לעבד את הבקשה.";
    addToHistory(chatId, "assistant", reply);

    bot.sendMessage(chatId, reply);
  } catch (err) {
    console.error("Error:", err.message);
    bot.sendMessage(chatId, "⚠️ שגיאה פנימית. נסה שוב.");
  }
});

